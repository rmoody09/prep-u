                    <p>
                        <math>
                            <mi>Perimeter</mi><mo>=</mo><mn>2</mn><mo>×</mo><mi>length</mi><mo>+</mo><mn>2</mn><mo>×</mo><mi>width</mi>
                        </math>
                    </p>
                    <p>
                        <strong>Square:</strong> Since all sides of a square are equal in length, the perimeter of a square is four times the length of one side:
                    </p>
                    <p>
                        <math>
                            <mi>Perimeter</mi><mo>=</mo><mn>4</mn><mo>×</mo><mi>side</mi>
                        </math>
                    </p>
