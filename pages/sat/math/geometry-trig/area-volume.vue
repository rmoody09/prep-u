                <h3 class="sat-guide-subsection-header">Surface Area of Common Shapes</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        <strong>Rectangular Prism:</strong> The surface area of a rectangular prism is the sum of the areas of all six faces. Since opposite faces are equal in area, the formula simplifies to:
                    </p>
                    <p>
                        <math>
                            <mi>Surface Area</mi><mo>=</mo><mn>2</mn><mo>×</mo><mi>length</mi><mo>×</mo><mi>width</mi><mo>+</mo><mn>2</mn><mo>×</mo><mi>length</mi><mo>×</mo><mi>height</mi><mo>+</mo><mn>2</mn><mo>×</mo><mi>width</mi><mo>×</mo><mi>height</mi>
                        </math>
                    </p>
                    <p>
                        <strong>Cube:</strong> Since all faces of a cube are equal in area, the surface area of a cube is six times the square of the length of one side:
                    </p>
                    <p>
                        <math>
                            <mi>Surface Area</mi><mo>=</mo><mn>6</mn><mo>×</mo><msup><mi>side</mi><mn>2</mn></msup>
                        </math>
                    </p>
                    <p>
                        <strong>Triangular Prism:</strong> The surface area of a triangular prism is the sum of the areas of the two triangular bases and the three rectangular faces:
                    </p>
                    <p>
                        <math>
                            <mi>Surface Area</mi><mo>=</mo><mn>2</mn><mo>×</mo><mfrac><mn>1</mn><mn>2</mn></mfrac><mo>×</mo><mi>base</mi><mo>×</mo><mi>height</mi><mo>+</mo><mi>perimeter</mi><mo>×</mo><mi>height</mi>
                        </math>
                    </p>
                    <p>
                        where <math><mi>base</mi></math> and <math><mi>height</mi></math> refer to the triangular base, and <math><mi>perimeter</mi></math> is the perimeter of the triangular base.
                    </p>
                    <p>
                        <strong>Note:</strong> The formulas for the surface area of a rectangular prism and cube are not typically provided on the SAT reference sheet, but they can be derived from the area formulas for rectangles and squares.
                    </p>
