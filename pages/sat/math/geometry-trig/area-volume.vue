            Area
        </h2>
        <div class="sat-guide-section-content">
            <p>
                The area of a shape is the amount of space it occupies in a two-dimensional plane. It is measured in square units such as square inches, square feet, square meters, or square centimeters.
            </p>

            <div class="sat-guide-subsection">
                <h3 class="sat-guide-subsection-header">Area of Common Shapes</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        <strong>Rectangle:</strong> The area of a rectangle is the product of its length and width:
                    </p>
                    <p>
                        <math>
                            <mi>Area</mi><mo>=</mo><mi>length</mi><mo>×</mo><mi>width</mi>
                        </math>
                    </p>
                    <p>
                        <strong>Square:</strong> Since all sides of a square are equal in length, the area of a square is the square of the length of one side:
                    </p>
                    <p>
                        <math>
                            <mi>Area</mi><mo>=</mo><msup><mi>side</mi><mn>2</mn></msup>
                        </math>
                    </p>
                    <p>
                        <strong>Triangle:</strong> The area of a triangle is half the product of its base and height:
                    </p>
                    <p>
                        <math>
                            <mi>Area</mi><mo>=</mo><mfrac><mn>1</mn><mn>2</mn></mfrac><mo>×</mo><mi>base</mi><mo>×</mo><mi>height</mi>
                        </math>
                    </p>
                    <p>
                        <strong>Circle:</strong> The area of a circle is the product of π and the square of its radius:
                    </p>
                    <p>
                        <math>
                            <mi>Area</mi><mo>=</mo><mi>π</mi><msup><mi>r</mi><mn>2</mn></msup>
                        </math>
                    </p>
                    <p>
                        where <math><mi>r</mi></math> is the radius of the circle.
                    </p>
                    <p>
