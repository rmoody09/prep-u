                    </p>
                    <ul>
                        <li>If linear dimensions are multiplied by a factor of <math><mi>k</mi></math>, then:</li>
                        <li>The area and surface area are multiplied by a factor of <math><msup><mi>k</mi><mn>2</mn></msup></math>.</li>
                        <li>The volume is multiplied by a factor of <math><msup><mi>k</mi><mn>3</mn></msup></math>.</li>
                    </ul>
                    <p>
                        Example: If the radius of a sphere is doubled, its surface area is multiplied by a factor of 4, and its volume is multiplied by a factor of 8.
                    </p>
                </div>
