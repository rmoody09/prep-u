                            <mi>Volume</mi><mo>=</mo><mi>length</mi><mo>×</mo><mi>width</mi><mo>×</mo><mi>height</mi>
                        </math>
                    </p>
                    <p>
                        <strong>Cube:</strong> Since all sides of a cube are equal in length, the volume of a cube is the cube of the length of one side:
                    </p>
                    <p>
                        <math>
                            <mi>Volume</mi><mo>=</mo><msup><mi>side</mi><mn>3</mn></msup>
                        </math>
                    </p>
                    <p>
                        <strong>Triangular Prism:</strong> The volume of a triangular prism is the product of the area of the triangular base and the height of the prism:
                    </p>
                    <p>
                        <math>
                            <mi>Volume</mi><mo>=</mo><mfrac><mn>1</mn><mn>2</mn></mfrac><mo>×</mo><mi>base</mi><mo>×</mo><mi>height</mi><mo>×</mo><mi>height</mi>
                        </math>
                    </p>
                    <p>
                        where the first <math><mi>base</mi></math> and <math><mi>height</mi></math> refer to the triangular base, and the second <math><mi>height</mi></math> refers to the height of the prism.
                    </p>
                    <p>
                        <strong>Pyramid:</strong> The volume of a pyramid is one-third the product of the area of the base and the height of the pyramid:
                    </p>
                    <p>
                        <math>
                            <mi>Volume</mi><mo>=</mo><mfrac><mn>1</mn><mn>3</mn></mfrac><mo>×</mo><mi>base area</mi><mo>×</mo><mi>height</mi>
                        </math>
                    </p>
                    <p>
                        <strong>Cylinder:</strong> The volume of a cylinder is the product of the area of the circular base and the height of the cylinder:
                    </p>
                    <p>
                        <math>
                            <mi>Volume</mi><mo>=</mo><mi>π</mi><msup><mi>r</mi><mn>2</mn></msup><mi>h</mi>
                        </math>
                    </p>
                    <p>
                        where <math><mi>r</mi></math> is the radius of the circular base, and <math><mi>h</mi></math> is the height of the cylinder.
                    </p>
                    <p>
                        <strong>Cone:</strong> The volume of a cone is one-third the product of the area of the circular base and the height of the cone:
                    </p>
                    <p>
                        <math>
                            <mi>Volume</mi><mo>=</mo><mfrac><mn>1</mn><mn>3</mn></mfrac><mi>π</mi><msup><mi>r</mi><mn>2</mn></msup><mi>h</mi>
                        </math>
                    </p>
                    <p>
                        where <math><mi>r</mi></math> is the radius of the circular base, and <math><mi>h</mi></math> is the height of the cone.
                    </p>
