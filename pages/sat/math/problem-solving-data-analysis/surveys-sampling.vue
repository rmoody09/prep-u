                    <p>
                        Several types of bias can affect the representativeness of a sample:
                    </p>
                    <ul>
                        <li>
                            <strong>Selection Bias:</strong> The sample is not representative of the population because of how it was selected.
                        </li>
                        <li>
                            <strong>Nonresponse Bias:</strong> People who choose not to respond to a survey may differ from those who do respond.
                        </li>
                        <li>
                            <strong>Volunteer Bias:</strong> People who volunteer to participate in a survey may differ from those who don't volunteer.
                        </li>
                        <li>
                            <strong>Response Bias:</strong> People may not answer survey questions honestly or accurately.
                        </li>
                    </ul>
                    <p>
                        On the SAT, you'll need to identify these biases and determine how they affect the generalizability of survey results.
                    </p>
                </div>
