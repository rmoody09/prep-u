    <div class="sat-guide-section">
        <h2 class="sat-guide-section-header">
            Understanding Surveys and Sampling
        </h2>
        <div class="sat-guide-section-content">
            <p>
                A survey is a method of collecting data from a subset of a larger population. The subset is called a sample, and the larger group it's drawn from is called the population. The goal of sampling is to make inferences about the entire population based on the data collected from the sample.
            </p>

            <div class="sat-guide-subsection">
                <h3 class="sat-guide-subsection-header">Population vs. Sample</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        <strong>Population:</strong> The entire group of individuals, items, or data points that you want to study. For example, all registered voters in a country, all students in a school, or all products manufactured by a company.
                    </p>
                    <p>
                        <strong>Sample:</strong> A subset of the population that is actually surveyed or measured. For example, 1,000 registered voters, 100 students, or 50 products.
                    </p>
                    <p>
                        It's important to understand that we rarely survey an entire population because:
                    </p>
                    <ul>
                        <li>It's often impractical or impossible to survey everyone</li>
                        <li>It's usually unnecessary - a well-designed sample can provide accurate results</li>
                        <li>It's more cost-effective to survey a smaller group</li>
                    </ul>
                </div>
            </div>

            <div class="sat-guide-subsection">
                <h3 class="sat-guide-subsection-header">Sampling Methods</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        There are several methods for selecting a sample from a population. The method used affects how well the sample represents the population.
                    </p>
                    <ul>
                        <li>
                            <strong>Random Sampling:</strong> Every member of the population has an equal chance of being selected. This is the ideal method for ensuring a representative sample.
                        </li>
                        <li>
                            <strong>Stratified Sampling:</strong> The population is divided into subgroups (strata) based on certain characteristics, and then random samples are taken from each subgroup.
                        </li>
                        <li>
                            <strong>Systematic Sampling:</strong> Every nth member of the population is selected (e.g., every 10th person on a list).
                        </li>
                        <li>
                            <strong>Convenience Sampling:</strong> Members of the population are selected based on their availability or ease of access. This method often leads to biased results.
                        </li>
                        <li>
                            <strong>Volunteer Sampling:</strong> Members of the population self-select to participate. This method often leads to biased results.
                        </li>
                    </ul>
                    <p>
                        On the SAT, you'll need to evaluate whether a sampling method is appropriate for making inferences about a population.
                    </p>
                </div>
            </div>
        </div>
    </div>

    <div class="sat-guide-section">
        <h2 class="sat-guide-section-header">
            Drawing Estimates from Samples
        </h2>
        <div class="sat-guide-section-content">
            <p>
                When we collect data from a sample, we use that data to make estimates about the entire population. These estimates are not exact values but rather approximations with a certain level of uncertainty.
            </p>

            <div class="sat-guide-subsection">
                <h3 class="sat-guide-subsection-header">Point Estimates</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        A point estimate is a single value used to estimate a population parameter. For example:
                    </p>
                    <ul>
                        <li>The sample mean is a point estimate of the population mean</li>
                        <li>The sample proportion is a point estimate of the population proportion</li>
                    </ul>
                    <p>
                        Example: If a survey of 1,000 registered voters finds that 45% support a particular candidate, then 45% is a point estimate of the percentage of all registered voters who support that candidate.
                    </p>
                </div>
            </div>

            <div class="sat-guide-subsection">
                <h3 class="sat-guide-subsection-header">Margin of Error</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        The margin of error is a measure of the uncertainty in a point estimate. It represents the range of values within which the true population parameter is likely to fall.
                    </p>
                    <p>
                        On the SAT, you'll often see survey results presented with a margin of error, such as "45% ± 3%." This means that the true percentage in the population is likely to be between 42% and 48%.
                    </p>
                    <p>
                        The margin of error depends on several factors:
                    </p>
                    <ul>
                        <li><strong>Sample Size:</strong> Larger samples generally have smaller margins of error</li>
                        <li><strong>Population Size:</strong> For very large populations, the margin of error depends mainly on the sample size</li>
                        <li><strong>Confidence Level:</strong> Higher confidence levels result in larger margins of error</li>
                    </ul>
                    <p>
                        While you won't need to calculate the margin of error on the SAT, you should understand how to interpret it and how it affects the conclusions you can draw from survey results.
                    </p>
                </div>
            </div>

            <div class="sat-guide-subsection">
                <h3 class="sat-guide-subsection-header">Confidence Intervals</h3>
                <div class="sat-guide-subsection-content">
                    <p>
                        A confidence interval is a range of values that is likely to contain the true population parameter. It is calculated by adding and subtracting the margin of error from the point estimate.
                    </p>
                    <p>
                        Example: If a survey finds that 45% of respondents support a candidate with a margin of error of 3%, the 95% confidence interval is 42% to 48%. This means we can be 95% confident that the true percentage of all voters who support the candidate is between 42% and 48%.
                    </p>
                    <p>
                        On the SAT, you'll need to understand how to interpret confidence intervals and use them to make inferences about populations.
                    </p>
                </div>
            </div>
        </div>
    </div>

