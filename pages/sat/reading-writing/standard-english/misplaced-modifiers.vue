    <h2 class="sat-guide-section-header">Beware of Possessives</h2>
    <div class="section-description">
        Say you have correctly identified that the noun being modified is "John". You might see an answer choice that starts with "John's car", for example, which might at first glance look right because you see John at the beginning of the sentence. But in this case, it would actually be his car, not John himself, that is the subject, and so the answer would be incorrect if the modifier is describing John himself.
